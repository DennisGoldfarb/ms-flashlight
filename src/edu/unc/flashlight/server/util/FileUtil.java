package edu.unc.flashlight.server.util;

import java.io.BufferedInputStream;
import java.io.File;
import java.io.FileInputStream;
import java.io.IOException;
import java.io.InputStream;

import org.apache.commons.io.FileUtils;

import edu.unc.flashlight.shared.util.Constants;

public class FileUtil {
	
	public static String createPath(String filename) {
		return Constants.FILES_PATH + filename;
	}
	
	public static void deleteFile(String filename) {
		File f = new File(filename);
		if(f.exists() && f.isFile()){
			f.delete();
		}
	}
	
	public static void deleteFolder(String path) {
		try {
			FileUtils.deleteDirectory(new File(path));
		} catch (Exception e) {
			System.err.println(e.getMessage());
		}
	}
	
	public static void deleteOldFiles(Long purgeTime) {
		final File directory = new File(Constants.FILES_PATH);
	    if (directory.exists()){
	        final File[] listFiles = directory.listFiles();
	        for (File listFile : listFiles) {
	        	if (!listFile.getName().contains("FRF_") && listFile.lastModified() < purgeTime) {
	        		listFile.delete();
	        	}
	        }
	    }
	}
	
	public static int count(String filename) throws IOException {
	    InputStream is = new BufferedInputStream(new FileInputStream(filename));
	    try {
	        byte[] c = new byte[1024];
	        int count = 0;
	        int readChars = 0;
	        while ((readChars = is.read(c)) != -1) {
	            for (int i = 0; i < readChars; ++i) {
	                if (c[i] == '\n')
	                    ++count;
	            }
	        }
	        return count;
	    } finally {
	        is.close();
	    }
	}
}
